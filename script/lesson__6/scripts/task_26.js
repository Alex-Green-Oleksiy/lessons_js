"use strict";

// 26. З клавіатури вводяться числа та послідовно сумуються.
//  Знайти кількість введених користувачем чисел, коли їх сума перевищить 100.

let count = 0
let sum = 0

do {
  sum += parseFloat(prompt('Введіть число', '20'))
  count++
} while (sum <= 100)

document.write(`Ви ввели ${count} чисел, аж доки не перевищили 100 у сумі.`)


